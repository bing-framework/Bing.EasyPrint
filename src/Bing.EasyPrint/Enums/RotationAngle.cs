// ReSharper disable once CheckNamespace
namespace Bing.EasyPrint;

/// <summary>
/// 旋转角度
/// </summary>
public enum RotationAngle
{
    /// <summary>
    /// 正常
    /// </summary>
    None = 0,
    /// <summary>
    /// 旋转90°
    /// </summary>
    Rotate90 = 90,
    /// <summary>
    /// 旋转180°
    /// </summary>
    Rotate180 = 180,
    /// <summary>
    /// 旋转270°
    /// </summary>
    Rotate270 = 270,
}